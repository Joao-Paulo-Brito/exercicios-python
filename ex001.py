"""
Exercício 001:
    Peça pro usuário digitar 3 valores inteiros e imprima a soma deles
"""
# Entrada de dados
print("Digite um valor: ")
numero1 = int(input())

print("Digite outro valor: ")
numero2 = int(input())

print("Digite mais um valor: ")
numero3 = int(input())

# Soma
soma = int(numero1+numero2+numero3)

# Saída de dados
print(f"A soma entre {numero1}, {numero2} e {numero3} é igual a: {soma}")
